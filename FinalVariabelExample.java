public class finalAtribut { 
    public static void main(String[] args) 
    { 
        // variabel biasa
        int a = 5; 
  
        // variable final
        final int b = 6; 
  
        // modifikasi variabel biasa
        a++; 
  
        // modifasi variabel final
        // akan menghasilkan error
        b++; 
    } 
}
